#!/usr/bin/env node
/*
 * readme-benchmark.mjs — regenerate the README benchmark block from the
 * published, validated artifact so the numbers in the README can never drift
 * away from the campaign that produced them.
 *
 * The block is delimited in README.md (and any translation) by:
 *   <!-- BENCH:START -->  ... generated ...  <!-- BENCH:END -->
 *
 * Modes:
 *   node scripts/readme-benchmark.mjs            # rewrite the block in README.md
 *   node scripts/readme-benchmark.mjs --check    # exit 1 if README is out of date
 *   node scripts/readme-benchmark.mjs --print    # print the block to stdout
 *   node scripts/readme-benchmark.mjs --files a.md b.md   # operate on given files
 *
 * The benchmark numbers are ENGLISH-canonical: translations embed the same block
 * verbatim (translation rule: never silently change benchmark numbers).
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const SUMMARY = join(ROOT, "benchmarks", "published", "campaign-2026-06-20.summary.json");
const START = "<!-- BENCH:START -->";
const END = "<!-- BENCH:END -->";

const argv = process.argv.slice(2);
const mode = argv.includes("--check") ? "check" : argv.includes("--print") ? "print" : "write";
const filesArgIdx = argv.indexOf("--files");
const files =
  filesArgIdx >= 0
    ? argv.slice(filesArgIdx + 1).map((f) => join(ROOT, f))
    : [join(ROOT, "README.md")];

// ---- formatting ------------------------------------------------------------
function fmt(kind, x) {
  if (x == null) return "n/a";
  if (kind === "rate") return `${Math.round(x * 100)}%`;
  if (kind === "ms") {
    const sec = x / 1000;
    return sec >= 1 ? `${(Math.round(sec * 10) / 10).toString()} s` : `${Math.round(x)} ms`;
  }
  if (kind === "tok") return Math.round(x).toLocaleString("en-US");
  return (Math.round(x * 10) / 10).toString();
}
function fmtDelta(kind, m) {
  if (m.deltaAbs == null) return "n/a";
  const sign = (v) => (v > 0 ? "+" : "") + v;
  if (kind === "rate") return `${sign(Math.round(m.deltaAbs * 100))} pp`;
  if (kind === "ms") {
    const sec = m.deltaAbs / 1000;
    return Math.abs(sec) >= 1 ? `${sign(Math.round(sec * 10) / 10)} s` : `${sign(Math.round(m.deltaAbs))} ms`;
  }
  if (kind === "tok") return sign(Math.round(m.deltaAbs));
  return sign(Math.round(m.deltaAbs * 10) / 10);
}

// metrics shown in the per-agent headline table, in order
const HEADLINE_ROWS = [
  "successRate",
  "kawnAutoInvoked",
  "filesFound",
  "openedPrecision",
  "filesOpened",
  "toolCalls",
  "timeToFirstRelevantMs",
  "wallMs",
  "outputTokens",
];

function agentTable(cell) {
  const L = [];
  L.push(`| Metric | Without KawnGraph | With KawnGraph | Difference |`);
  L.push(`| --- | --- | --- | --- |`);
  for (const key of HEADLINE_ROWS) {
    const m = cell.metrics[key];
    if (!m) continue;
    L.push(`| ${m.label} | ${fmt(m.kind, m.without)} | ${fmt(m.kind, m.with)} | ${fmtDelta(m.kind, m)} |`);
  }
  return L.join("\n");
}

function renderBlock(s) {
  const claude = s.cells.find((c) => c.agent === "claude" && c.headline);
  const codex = s.cells.find((c) => c.agent === "codex" && c.headline);
  const ht = s.headlineTask;
  const L = [];
  L.push(START);
  L.push("");
  L.push(
    `<!-- Generated by scripts/readme-benchmark.mjs from benchmarks/published/campaign-${s.createdAt.slice(0, 10)}.summary.json — do not edit by hand. -->`,
  );
  L.push("");
  L.push(
    `Local A/B harness, ${s.validation.usableRuns} agent sessions, seed ${s.seed}, ${s.repeat} repeats per arm ` +
      `(${s.validation.minSamplePerArm}/arm after grouping — **exploratory, n<5, directional only**). ` +
      `Same agent, same task, same repository snapshot; A = without KawnGraph, B = with. Δ = B − A. ` +
      `Gold validation: ${s.validation.goldValidation}.`,
  );
  L.push("");
  L.push(`**Headline task — \`${ht.taskId}\` (${ht.mode}) on \`${ht.projectId}\`:**`);
  L.push("");
  L.push(`*Claude Code — same task, same repository, same model (model not pinned in artifact):*`);
  L.push("");
  L.push(agentTable(claude));
  L.push("");
  L.push(`*Codex — same task, same repository, same model (model not pinned in artifact):*`);
  L.push("");
  L.push(agentTable(codex));
  L.push("");
  L.push(`> KawnGraph is task-dependent. It can reduce repository exploration on unfamiliar multi-file work, while adding overhead on already-focused tasks. See the full methodology and limitations in [docs/BENCHMARKS.md](docs/BENCHMARKS.md).`);
  L.push("");
  L.push(`**Where it helped, was neutral, or hurt (all ${s.cells.length} task cells):**`);
  L.push("");
  L.push(`| Task family | Agent | Mode | Outcome | Tool-call Δ | Time Δ |`);
  L.push(`| --- | --- | --- | --- | --- | --- |`);
  for (const c of s.cells) {
    L.push(
      `| ${c.taskId} | ${c.agent} | ${c.mode} | ${c.outcome} | ${fmtDelta("num", c.metrics.toolCalls)} | ${fmtDelta("ms", c.metrics.wallMs)} |`,
    );
  }
  L.push("");
  L.push(
    `Outcome labels (\`Improved\` / \`Neutral\` / \`Regressed\` / \`Insufficient data\`) are derived ` +
      `deterministically from tool-call and wall-time deltas; every cell is n=${s.validation.minSamplePerArm}/arm, so all are directional. ` +
      `Full per-metric tables: [benchmarks/published/campaign-${s.createdAt.slice(0, 10)}.md](benchmarks/published/campaign-${s.createdAt.slice(0, 10)}.md).`,
  );
  L.push("");
  L.push(END);
  return L.join("\n");
}

// ---- apply -----------------------------------------------------------------
const summary = JSON.parse(readFileSync(SUMMARY, "utf8"));
const block = renderBlock(summary);

if (mode === "print") {
  process.stdout.write(block + "\n");
  process.exit(0);
}

let drift = false;
for (const file of files) {
  if (!existsSync(file)) {
    if (mode === "check") continue;
    console.error(`[readme-benchmark] skip (missing): ${file}`);
    continue;
  }
  const src = readFileSync(file, "utf8");
  const si = src.indexOf(START);
  const ei = src.indexOf(END);
  if (si < 0 || ei < 0) {
    console.error(`[readme-benchmark] no BENCH markers in ${file} — add ${START} … ${END}`);
    drift = true;
    continue;
  }
  const before = src.slice(0, si);
  const after = src.slice(ei + END.length);
  const next = before + block + after;
  if (next === src) {
    console.log(`[readme-benchmark] up to date: ${file}`);
    continue;
  }
  if (mode === "check") {
    console.error(`[readme-benchmark] OUT OF DATE: ${file} — run \`node scripts/readme-benchmark.mjs\``);
    drift = true;
  } else {
    writeFileSync(file, next, "utf8");
    console.log(`[readme-benchmark] updated: ${file}`);
  }
}

process.exit(drift ? 1 : 0);
